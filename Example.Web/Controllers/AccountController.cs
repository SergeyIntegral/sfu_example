        //
        // POST: /Account/Login
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model, string returnUrl)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            var user = await UserManager.FindByEmailAsync(model.Email);
            if (user != null)
            {
                var validCredentials = await UserManager.FindAsync(model.Email, model.Password);

                if (await UserManager.IsLockedOutAsync(user))
                {
                    ModelState.AddModelError("", string.Format("Account is blocked for {0} minutes", UserManager.DefaultAccountLockoutTimeSpan.TotalMinutes));
                }
                else if (validCredentials != null)
                {
                    await UserManager.ResetAccessFailedCountAsync(user);
                    await SignInAsync(user, model.RememberMe);
                    return RedirectToLocal(returnUrl);
                }
                else
                {
                    // Record the failure which also may cause the user to be locked out
                    await UserManager.AccessFailedAsync(user);
                    await UserManager.UpdateAsync(user);
                    string message;
                    if (await UserManager.IsLockedOutAsync(user))
                    {
                        message = string.Format("Account is blocked for {0} minutes", UserManager.DefaultAccountLockoutTimeSpan.TotalMinutes);
                    }
                    else
                    {
                        await UserManager.GetAccessFailedCountAsync(user);
                        message = "Login Failed";
                    }
                    ModelState.AddModelError("", message);
                }
            }
            else
            {
                ModelState.AddModelError("", "Login Failed");
            }

            // If we got this far, something failed, redisplay form
            return View(model);
        }

